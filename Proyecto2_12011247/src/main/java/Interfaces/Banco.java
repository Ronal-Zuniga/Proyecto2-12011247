    ArrayList<Persona> people = new ArrayList<>();
    Cola cola = new Cola();
    Random r = new Random();
    ArrayList<Cola> colas = new ArrayList<>();

    public void registro() {
        String name = "Persona";
        for (int i = 1; i < 64; i++) {
            Persona pr = new Persona(name, i);
            people.add(pr);
        }
    }

    public void unaFila() {
        for (int i = 0; i < people.size(); i++) {
            Persona p = people.get(i);
            TipoElemento te = new TipoElemento(p);
            cola.mete(te);
        }
    }

    public void filasPorCajero() {
        Cola cl1 = new Cola();
        Cola cl2 = new Cola();
        Cola cl3 = new Cola();
        for (int i = 0; i < people.size(); i += 3) {
            Persona p = people.get(i);
            Persona pl2 = people.get(i + 1);
            Persona pl3 = people.get(i + 2);
            TipoElemento te = new TipoElemento(p);
            TipoElemento te2 = new TipoElemento(pl2);
            TipoElemento te3 = new TipoElemento(pl3);
            cl1.mete(te);
            cl2.mete(te2);
            cl3.mete(te3);
        }
        colas.add(cl3);
        colas.add(cl2);
        colas.add(cl1);
    }

    public int numRandom() {
        int n = 1 + r.nextInt(3);
        return n;
    }

    public String transaccion(int n) {
        String t = "";
        switch (n) {
            case 1:
                t = "Deposito";
                break;

            case 2:
                t = "Retiro";
                break;

            case 3:
                t = "Pago";
                break;
        }
        return t;
    }

